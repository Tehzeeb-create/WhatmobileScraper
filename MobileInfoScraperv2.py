import json
import os
import requests
from bs4 import BeautifulSoup



def extract_image_url(soup):
    image_url_tag = soup.select_one('img[src$=".jpg"]')
    return image_url_tag['src'] if image_url_tag else ''

def extract_mobile_name(link):
    return link.split('/')[-1]


def extract_company_name(link):
    start_index = link.find("https://www.whatmobile.com.pk/") + len("https://www.whatmobile.com.pk/")
    end_index = link.find("_", start_index)

    return link[start_index:end_index] if start_index != -1 and end_index != -1 else None


def download_image_from_source(link, image_dir):
    try:
        response = requests.get(link)

        if response.status_code == 200:
            soup = BeautifulSoup(response.text, 'html.parser')
            image_url = "https://www.whatmobile.com.pk/" + extract_image_url(soup)

            if image_url:
                mobile_name = extract_mobile_name(link)
                company_name = extract_company_name(link)

                clean_mobile_name = mobile_name.replace(' ', '_').replace('\n', '').replace('\r', '')
                image_filename = f"{clean_mobile_name}.jpg"
                image_output_path = os.path.join(image_dir, image_filename)

                response_image = requests.get(image_url, stream=True)
                with open(image_output_path, 'wb') as f:
                    for chunk in response_image.iter_content(chunk_size=8192):
                        f.write(chunk)

                print(f"Image downloaded successfully: {image_output_path}")
            else:
                print("Image URL not found on the source page.")
            return image_url, mobile_name, company_name
        else:
            print(f"Failed to fetch source page: {link}")
    except Exception as e:
        print(f"Error: {e}")

def scrape_and_save_to_excel(links, image_dir):
    try:
        information = []

        for link in links:
            response = requests.get(link)

            if response.status_code == 200:
                soup = BeautifulSoup(response.text, 'html.parser')
                checkForExistance = soup.select('div.Heading1')


                for elements in checkForExistance:
                    el = elements.find("table")

                    if el is not None:
                        mainDiv = el

                keys = mainDiv.select("tbody tr th.hdngArial.specs-subHeading.bottom-border")
                values = mainDiv.select("tbody tr td.specs-value.bottom-border")

                allKeys = [info.text.strip() for info in keys if info.text.strip() != "None"]
                allValues = [info.text.strip() for info in values]

                td_elements = soup.select('td.fasla.RowBG1.bottom-border-section.right-border.specs-subHeading')

                for td_element in td_elements:
                    if "Price in Rs:" in td_element.get_text():
                        strong_element = td_element.find_next('strong')

                        if strong_element:
                            price = strong_element.get_text(strip=True)
                        else:
                            price = None

                image_url, mobile_name , company_name = download_image_from_source(link, image_dir)
                informationDict = {}
                for i in range(len(allKeys)):
                    if len(informationDict.keys()) == 0:
                        informationDict = {
                            allKeys[i]: allValues[i],
                            "imageUrl":image_url,
                            "Price": price,
                            "Mobile Name":mobile_name,
                            "Company Name" : company_name
                        }
                    else:
                        informationDict.update({allKeys[i]: allValues[i]})
                information.append(informationDict)
                with open(os.path.abspath("json/information.json"), 'w') as f:
                    json.dump(information, f, indent=2)
                    f.close()
                image_url = extract_image_url(soup)



        return information
    except Exception as e:
        print("Error", e)

